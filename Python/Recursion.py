def factorial(N):
    if N == 1:   #O(1)
        return 1 #O(1)
    elif N == 0: #O(1)
        return 1 #O(1)
    else:
        return N * factorial(N-1) #O(n)
    
inp = int(input("Enter the number: ")) #O(n)
print("the factorial of the given: ",inp," is: ",factorial(inp)) #O(n)

''' Total contribution is O (n)'''